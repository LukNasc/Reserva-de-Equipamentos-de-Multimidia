    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(tfcod.getText().equals("") || tfcod.getText().equals(0)){
                JOptionPane.showMessageDialog(null,"O campo \"ID\" não pode ser vazío e não pode ser igual a 0",
                        "Erro: campo de ID",JOptionPane.ERROR_MESSAGE);
            }else{
                if(tfnome.getText().equals("") ){
                    JOptionPane.showMessageDialog(null,"Você não Pode deixar o campo de \"Nome\" Vazío",
                            "Erro: campo de nome",JOptionPane.ERROR_MESSAGE);
                }else{
                    if(tfdisc.getText().equals("")){
                        JOptionPane.showMessageDialog(null,"Você não Pode deixar o campo de \"Disciplina\""
                                + " Vazío","Erro: campo de disciplina",JOptionPane.ERROR_MESSAGE);
                    }else{
                        if(tfturma.getText().equals("")){
                            JOptionPane.showMessageDialog(null,"Você não Pode deixar o campo de \"Turma\""
                                    + " Vazío","Erro: campo de turma",JOptionPane.ERROR_MESSAGE);
                        }else{
                            ProfessorControl controle=new ProfessorControl();
                            ProfessorModel modelo=new ProfessorModel();
                            modelo.setCod(Integer.parseInt(tfcod.getText()));
                            modelo.setNome(tfnome.getText());
                            modelo.disc.setNome(tfdisc.getText());
                            modelo.turma.setNome(tfturma.getText());
                            controle.EditarProfessor(modelo);
                           try{
                               int cod=Integer.parseInt(tfcod.getText());
                                controle.ListarProfessor().get(cod);
                                JOptionPane.showMessageDialog(null,"Editado com sucesso","Sucesso - ID:"
                                        +tfcod.getText(),
                                        JOptionPane.INFORMATION_MESSAGE);
                            }catch(IndexOutOfBoundsException e){
                              JOptionPane.showMessageDialog(null,"O ID digitado não existe","ID: "
                                      +tfcod.getText(),
                                      JOptionPane.ERROR_MESSAGE);
                           }
                            tfcod.setText("");
                            tfnome.setText("");
                            tfdisc.setText("");
                            tfturma.setText("");
                            atualizaTabela();
                        }
                    }
                }
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Há inputs que só podem receber número inteiros \n "
                    + "verifique se você não digitou nada de errado e tente novamente","Não é permitido"
                            + " letras",JOptionPane.ERROR_MESSAGE);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null,"O Valor digitado mão existe","Valor não existe",
                    JOptionPane.ERROR_MESSAGE);
          ;
        }catch(IllegalArgumentException e){
            JOptionPane.showMessageDialog(null, "Você digitou algum argumento com valores inválido \n "
                    + "verifique suas entradas e tente novamente","Valor Inválido",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

