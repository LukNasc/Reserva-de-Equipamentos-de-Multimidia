private void  atualizaTabela(){
ProfessorControl controle = new ProfessorControl();
        ArrayList<ProfessorModel> lista = new ArrayList<>();
        lista = controle.ListarProfessor();
        DefaultTableModel tbm = (DefaultTableModel) table.getModel(); // Armazena a tabela da tela
            while(tbm.getRowCount() > 0){
            	tbm.removeRow(0);       
         }
         int i = 0;
        for(ProfessorModel prof : lista){
            tbm.addRow(new String[i]);
            table.setValueAt(prof.getCod(), i, 0);
            table.setValueAt(prof.getNome(), i, 1);
            table.setValueAt(prof.disc.getNome(), i, 2);
            table.setValueAt(prof.turma.getNome(), i, 3);
            i++;
        }
}
